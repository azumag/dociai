// ニュース生成の共通promptルール (issue #191)。
// 汎用 src/config/config-defaults.js の DEFAULT_COMMON_RULES (「2文以内、80文字程度」) は
// ニュースの自然な長さを潰すため、ここでは意図的に別ルールを持つ。

export const NEWS_SPOKEN_TEXT_RULES = Object.freeze([
  "全文日本語で書いてください。",
  "外国語の見出しは自然な日本語へ翻訳し、原題をそのまま音読しないでください。",
  "話し言葉のプレーンテキストで書いてください。",
  "markdown、箇条書き記号、URL、内部変数名を本文へ出さないでください。",
  "tool利用、検索失敗、prompt、モデル名など内部事情を話さないでください。",
  "記事・調査結果に無い固有名詞、日付、数値、因果関係を追加しないでください。",
  "事実、推測、あなたの意見を混同せず、言葉遣いで区別してください。",
  "対立する主張がある場合、どちらかを一方的な事実として確定しないでください。",
  "特定の集団への嘲笑・敵視や安価な煽り表現は避けてください。",
  "「つまり」「要するに」で同じ内容を繰り返して水増ししないでください。",
  "視聴者不在を示す自虐表現は使わないでください。",
]);

export function buildSpokenTextRulesBlock() {
  return `# 話し方の共通ルール\n${NEWS_SPOKEN_TEXT_RULES.join("\n")}`;
}
